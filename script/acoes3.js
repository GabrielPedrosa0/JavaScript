let c1 = document.getElementById('c1')
let desc1 = c1.querySelector('p')
let btn1 = c1.querySelector('button')

    c1.addEventListener('mouseenter', () => {
        desc1.style.display = 'block'
    })

    c1.addEventListener('mouseout', () => {
        desc1.style.display = 'none'
    })

    desc1.addEventListener('mouseenter', () => {
        desc1.style.display = 'block'
    })

    btn1.addEventListener('mouseenter', () => {
        desc1.style.display = 'block'
    })

    desc1.addEventListener('mosemove', () => {
        desc1.style.display = 'block'
    })

